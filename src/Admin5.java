    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        // TODO add your handling code here:
        Random random=new Random();
        String accountNumber=""+Math.abs((random.nextLong()%90000000L)+123493600000000L);
        String firstName=firstNameTextField.getText();
        String lastName=lastNameTextField.getText();
        String fathersName=fathersNameTextField.getText();
        String dateOfBirth=((JTextField)dateOfBirthDateChooser.getDateEditor().getUiComponent()).getText();
        String gender=null;
        if(maleRadioButton.isSelected())
        {
            gender="Male";
        }else if(femaleRadioButton.isSelected())
        {
            gender="Female";
        }
        String maritalStatus=null;
        if(marriedRadioButton.isSelected())
        {
            maritalStatus="Married";
        }else if(unmarriedRadioButton.isSelected())
        {
            maritalStatus="Unmarried";
        }else if(otherRadioButton.isSelected())
        {
            maritalStatus="Other";
        }
        String phoneNumber=phoneNumberTextField.getText();
        String emailAddress=emailAddressTextField.getText();    
        
        try{
            if(firstName.equals("")||
                lastName.equals("")||
                fathersName.equals("")||
                dateOfBirth.equals("")||
                gender.equals("")||
                maritalStatus.equals("")||
                phoneNumber.equals("")||
                emailAddress.equals(""))
            {
                if(firstName.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"First Name is Requied");     
                }
                if(lastName.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Last Name is Requied");     
                }
                if(fathersName.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Father's Name is Requied");     
                }
                if(dateOfBirth.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Date of Birth is Requied");     
                }
                if(gender.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Gender is Requied");     
                }
                if(maritalStatus.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Marital Status is Requied");     
                }
                if(phoneNumber.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Phone Number is Requied");     
                }
                if(emailAddress.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Email Address is Requied");     
                }   
            }else{
                Conn c=new Conn();
                String query="insert into addApplication1 values('"+accountNumber+"', '"+firstName+"', '"+lastName+"', '"+fathersName+"', '"
                        +dateOfBirth+"', '"+gender+"', '"+maritalStatus+"', '"+phoneNumber+"', '"+emailAddress+"')";
                c.s.executeUpdate(query);
                
                Admin6 A6= new Admin6(accountNumber);
                setVisible(false);
                A6.setVisible(true);
                dispose();
            }
        }catch(Exception e){
            System.out.println(e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_nextButtonActionPerformed

