    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        // TODO add your handling code here:
        String presentAddress=presentAddressTextField.getText();
        String permanentAddress=permanentAddressTextField.getText();
        String city=cityTextField.getText();
        String postCode=postCodeTextField.getText();
        String religion=(String)religionComboBox.getSelectedItem();
        String nationality=nationalityTextField.getText();
        String educationQualification=(String)educationQualificationComboBox.getSelectedItem();
        String occupation=(String)occupationComboBox.getSelectedItem();    
        
        try{
            if(presentAddress.equals("")||
                permanentAddress.equals("")||
                city.equals("")||
                postCode.equals("")||
                religion.equals("Select")||
                nationality.equals("")||
                educationQualification.equals("Select")||
                occupation.equals("Select"))
            {
                if(presentAddress.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Present Address is Requied");     
                }
                if(permanentAddress.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Permanent Address is Requied");     
                }
                if(city.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"City is Requied");     
                }
                if(postCode.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Post Code is Requied");     
                }
                if(religion.equals("Select"))
                {
                     JOptionPane.showMessageDialog(null,"Religion is Requied");     
                }
                if(nationality.equals(""))
                {
                     JOptionPane.showMessageDialog(null,"Nationality is Requied");     
                }
                if(educationQualification.equals("Select"))
                {
                     JOptionPane.showMessageDialog(null,"Education Qualification is Requied");     
                }
                if(occupation.equals("Select"))
                {
                     JOptionPane.showMessageDialog(null,"Occupation is Requied");     
                }   
            }else{
                Conn c=new Conn();
                String query="insert into addApplication2_1 values('"+accountNumber+"', '"+presentAddress+"', '"+permanentAddress+"', '"+city+"', '"
                        +postCode+"', '"+religion+"', '"+nationality+"', '"+educationQualification+"', '"+occupation+"')";
                c.s.executeUpdate(query);
                
                Admin7 A7= new Admin7(accountNumber);
                setVisible(false);
                A7.setVisible(true);
                dispose();
            }
        }catch(Exception e){
            System.out.println(e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_nextButtonActionPerformed

